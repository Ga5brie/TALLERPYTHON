#ESCRIBA UN PROGRAMA QUE LEA SI EL NUMERO ES POSITIVO, NEGATIVO O CERO
print("Hola Usuario")
print("Se le solicitara un numero a su eleccion :)")
num1=int(input("Ingrese su Numero:"))
